  const sh = (t, yy, col=[72,69,210]) => {
    doc.setFillColor(244,246,251); doc.rect(M, yy-5, W-M*2, 8, 'F');
    doc.setFillColor(...col); doc.rect(M, yy-5, 3, 8, 'F');
    doc.setFont('helvetica','bold'); doc.setFontSize(7.5); doc.setTextColor(82,88,120);
    doc.text(t, M+6, yy);
    return yy + 10;
  };

  const trow = (lbl, val, yy) => {
    doc.setFillColor(244,246,251); doc.rect(M, yy-4, W-M*2, 8, 'F');
    doc.setFont('helvetica','bold'); doc.setFontSize(8); doc.setTextColor(82,88,120);
    doc.text(lbl, M+2, yy);
    doc.setFont('helvetica','normal'); doc.setFontSize(8.5); doc.setTextColor(26,29,46);
    const vl = doc.splitTextToSize(String(val||'—'), W-M*2-46);
    doc.text(vl[0]||'', M+46, yy);
    return yy + 8;
  };

  /* ─ 1. Problem Discription ─ */
  doc.setFont('helvetica','bold'); doc.setFontSize(13); doc.setTextColor(26,29,46);
  doc.text('1. Problem Discription', M, y); y += 8;
  doc.setFont('helvetica','normal'); doc.setFontSize(9.5); doc.setTextColor(82,88,120);
  const introLines = doc.splitTextToSize(d.intro || '—', W-M*2);
  doc.text(introLines, M, y);
  y += introLines.length * 5.5 + 6;
  doc.setFillColor(226,230,239); doc.rect(M, y, W-M*2, .5, 'F'); y += 6;

  /* ─ 2. Problem Statement ─ */
  checkY(50);
  doc.setFont('helvetica','bold'); doc.setFontSize(13); doc.setTextColor(26,29,46);
  doc.text('2. PROBLEM STATEMENT', M, y); y += 8;

  /* Blue table header */
  doc.setFillColor(72,69,210); doc.rect(M, y-5, W-M*2, 8, 'F');
  doc.setFont('helvetica','bold'); doc.setFontSize(7); doc.setTextColor(255,255,255);
  doc.text('FIELD', M+2, y); doc.text('VALUE', M+48, y); y += 5;

  y = trow('Part Name',                  d.part,                         y);
  y = trow('Problem Description',        d.prob,                         y);
  y = trow('Complaint Date',             d.date,                         y);
  y = trow('Location',                   d.loc,                          y);
  y = trow('Vehicle Number',             d.vin,                           y);
   y = trow('ODO',                       d.odo,                          y);
  y = trow('Failed Part',                d.failed,                        y);
  y = trow('Operator',                   d.op,                           y);
  y = trow('DTC Code(s)',                `DTC ${d.dtc.code} — ${d.dtc.name}`, y);
  y += 6;
  doc.setFillColor(226,230,239); doc.rect(M, y, W-M*2, .5, 'F'); y += 6;

  /* ─ 3. Root Cause Analysis ─ */
  checkY(20);
  doc.setFont('helvetica','bold'); doc.setFontSize(13); doc.setTextColor(26,29,46);
  doc.text('3. ROOT CAUSE ANALYSIS', M, y); y += 8;
  doc.setFont('helvetica','normal'); doc.setFontSize(9); doc.setTextColor(82,88,120);
  doc.text(`DTC ${d.dtc.code} (${d.dtc.name}) triggered. Action: ${d.dtc.action}.`, M, y); y += 6;
  doc.text(`Diagnostic performed across all ${d.dtc.cases.length} cases. See observations below.`, M, y); y += 10;
