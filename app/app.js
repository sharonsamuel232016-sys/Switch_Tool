/* ══════════════════════════════════════════════════════════════════════════
   7. CASE PROGRESS BAR
   Builds numbered circles for each case, updates colour as user progresses.
══════════════════════════════════════════════════════════════════════════ */

function buildCaseBar(dtc) {
  const bar = document.getElementById('caseBar');
  bar.innerHTML = '';

  dtc.cases.forEach((c, i) => {
    if (i > 0) {
      const sep = document.createElement('div');
      sep.className = 'cs-sep';
      sep.id = 'csep' + i;
      bar.appendChild(sep);
    }
    const s = document.createElement('div');
    s.className = 'cs';
    s.id = 'cst' + i;
    s.innerHTML = `<div class="cs-n" title="${c.area_detail}">${i + 1}</div><span class="cs-l">${c.area_detail || c.sheet}</span>`;
    s.onclick = () => { if (i <= currentCase || answers[i]) navigateCase(i); };
    bar.appendChild(s);
  });
  updateCaseBar(0);
}

function updateCaseBar(active) {
  if (!activeDTC) return;
  activeDTC.cases.forEach((_, i) => {
    const el  = document.getElementById('cst' + i);
    if (!el) return;
    el.classList.remove('active', 'done');
    if (i < active)      el.classList.add('done');
    else if (i === active) el.classList.add('active');

    const sep = document.getElementById('csep' + i);
    if (sep) sep.style.background = i <= active ? 'var(--green)' : 'var(--border)';
  });

  const total = activeDTC.cases.length;
  document.getElementById('d-prog').textContent =
    active >= total ? 'Complete' : `Case ${active + 1} of ${total}`;
}

