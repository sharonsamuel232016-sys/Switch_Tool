function buildNL() {
  const grid = document.getElementById('nlGrid');
  NL_DATA.slice(1).forEach((n, i) => {
    const c = document.createElement('div');
    c.className = 'ic';
    c.innerHTML = `
      <div class="ic-top">
        <div class="ic-row">
          <div class="ic-num">Issue #${n.id} · ${n.date} · ${n.readTime}</div>
          ${n.isNew ? '<div class="ic-new">NEW</div>' : ''}
        </div>
        <div class="ic-title">${n.title}</div>
      </div>
      <div class="ic-body">
        <p class="ic-desc">${n.excerpt}</p>
        <div class="tags">${n.tags.map((t,j) => `<span class="tag ${n.tagColors[j]||''}">${t}</span>`).join('')}</div>
      </div>`;
    c.onclick = () => openReader(i + 1);
    grid.appendChild(c);
  });
}

function openReader(idx) {
  const n = NL_DATA[idx];
  if (!n) return;
  document.getElementById('rdMeta').textContent = `Issue #${n.id} · ${n.date} · ${n.readTime}`;
  document.getElementById('rdContent').innerHTML = `
    <div class="rd-tag">${n.tags.join(' · ')}</div>
    <div class="rd-title">${n.title}</div>
    <div class="rd-meta"><span>#${n.id}</span><span>${n.date}</span><span>${n.readTime}</span></div>
    <div class="rd-line"></div>
    ${n.sections.map(s => `
      <div class="rd-sec">
        <div class="rd-h2">${s.heading}</div>
        <div class="rd-body">${s.body.replace(/\n/g, '<br>')}</div>
      </div>`).join('')}`;
  const rd = document.getElementById('nlReader');
  rd.classList.add('open');
  rd.scrollTop = 0;
}

function closeReader() {
  document.getElementById('nlReader').classList.remove('open');
}

function nlSub(id) {
  const v = document.getElementById(id).value.trim();
  if (!v || !v.includes('@')) { alert('Please enter a valid email.'); return; }
  document.getElementById(id).value = '';
  const t = document.getElementById('toast');
  t.classList.add('show');
  setTimeout(() => t.classList.remove('show'), 3500);
}

