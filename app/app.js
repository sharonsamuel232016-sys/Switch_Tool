function startDiag() {
  const intro   = document.getElementById('fi-intro').value.trim();
  const part    = document.getElementById('fi-part').value.trim();
  const prob    = document.getElementById('fi-prob').value.trim();
  const vin     = document.getElementById('fi-vin').value.trim();
  const odo     = document.getElementById('fi-odo').value.trim();
  const op      = document.getElementById('fi-op').value.trim();

  /* Collect all checked DTC codes */
  const checkedCodes = [...document.querySelectorAll('#fi-dtc-list input[type=checkbox]:checked')]
    .map(cb => cb.value);

  if (!intro || !part || !prob || !vin || !op || checkedCodes.length === 0) {
    alert('Please fill all required fields (*) and select at least one DTC code.');
    return;
  }

  /* Build a combined "virtual" DTC from all selected codes */
  const selectedDTCs = checkedCodes.map(c => DTC_LIBRARY[c]).filter(Boolean);
  const combinedCases = selectedDTCs.flatMap(d => d.cases.map(c => ({ ...c, _dtcCode: d.code, _dtcName: d.name })));

  activeDTC = {
    code:        selectedDTCs.map(d => d.code).join(', '),
    name:        selectedDTCs.map(d => d.name).join(' / '),
    description: selectedDTCs.map(d => d.description).join(' | '),
    action:      [...new Set(selectedDTCs.map(d => d.action))].join(' / '),
    severity:    selectedDTCs[0].severity,
    category:    [...new Set(selectedDTCs.map(d => d.category))].join(', '),
    vehicles:    selectedDTCs[0].vehicles,
    cases:       combinedCases,
    _selectedDTCs: selectedDTCs   /* keep originals for export */
  };

  /* Save intake data */
  intake = {
    intro, part, prob,
    date:   document.getElementById('fi-date').value,
    loc:    document.getElementById('fi-loc').value  || '—',
    vin, odo: odo || '—',
    failed: document.getElementById('fi-failed') ? document.getElementById('fi-failed').value : 'To be determined',
    op, vehicle,
    dtcCodes: checkedCodes
  };

  document.getElementById('dOp').textContent = `Operator: ${op} · VIN: ${vin}`;

  currentCase = 0;
  answers     = {};
  buildCaseBar(activeDTC);
  renderCase(0);
}


/* ══════════════════════════════════════════════════════════════════════════
   9. RENDER CASE
   Builds the step cards for a given case index.
══════════════════════════════════════════════════════════════════════════ */

function renderCase(ci) {
  currentCase = ci;
  updateCaseBar(ci);

  const dtc = activeDTC;
  const c   = dtc.cases[ci];

  /* ── Build HTML for each step ── */
  const stepsHtml = c.steps.map((s, si) => {
    const ans      = answers[ci] && answers[ci][si];
    const answered = ans && ans.value != null && ans.value !== '';
    const flagged  = answered && (ans.value === 'NO' || ans.value === 'NOT OK');
    const cardCls  = answered ? (flagged ? 'flagged' : 'answered') : '';
    const notes    = (ans && ans.notes) ? ans.notes : '';

    /* Input widget based on input_type */
    let inputHtml = '';

    if (s.input_type === 'yesno') {
      const ysel = (answered && ans.value === 'YES')    ? 'sel' : '';
      const nsel = (answered && ans.value === 'NO')     ? 'sel' : '';
      inputHtml = `
        <div class="yn-btns">
          <button class="yn-btn yes ${ysel}" onclick="setAns(${ci},${si},'YES',this)">✓ YES</button>
          <button class="yn-btn no  ${nsel}" onclick="setAns(${ci},${si},'NO',this)">✗ NO</button>
        </div>`;

    } else if (s.input_type === 'condition') {
      const c1 = (answered && ans.value === 'OK')     ? 'sel' : '';
      const c2 = (answered && ans.value === 'NOT OK') ? 'sel' : '';
      inputHtml = `
        <div class="cond-btns">
          <button class="cond-btn ${c1}" onclick="setAns(${ci},${si},'OK',this)">✓ OK — Properly Mounted</button>
          <button class="cond-btn ${c2}" onclick="setAns(${ci},${si},'NOT OK',this)">✗ Not OK — Issue Found</button>
        </div>`;

    } else {
      /* number / resistance */
      const unit   = s.obs_label.toLowerCase().includes('resist') ? 'Ω' : '';
      const curVal = (ans && ans.value != null) ? ans.value : '';
      inputHtml = `
        <div class="num-wrap">
          <input class="num-input" type="number" step="any" value="${curVal}"
            placeholder="Enter reading..." id="num-${ci}-${si}"
            oninput="setAns(${ci},${si},this.value,this)">
          <span class="num-unit">${unit}</span>
        </div>`;
    }

    return `
      <div class="step-card ${cardCls}" id="sc-${ci}-${si}">
        <div class="sc-top">
          <div class="sc-num">${String(si + 1).padStart(2, '0')}</div>
          <div class="sc-body">
            <div class="sc-step">${s.step}</div>
            <div class="sc-obs-lbl">${s.obs_label}</div>
            ${inputHtml}
            <div class="sc-corrective ${flagged ? 'show' : ''}" id="corr-${ci}-${si}">⚡ ${s.corrective}</div>
            <div class="sc-ok ${(answered && !flagged) ? 'show' : ''}" id="ok-${ci}-${si}">✓ Observation recorded</div>
            <textarea class="sc-notes" id="notes-${ci}-${si}"
              placeholder="Add notes (optional)..."
              onblur="saveNotes(${ci},${si},this.value)">${notes}</textarea>
          </div>
        </div>
      </div>`;
  }).join('');

  const isLast = ci === dtc.cases.length - 1;

  document.getElementById('diagContent').innerHTML = `
    <div style="padding:18px 0 0;">

      <!-- DTC info banner -->
      <div class="dtc-hdr">
        <span class="dtc-badge">DTC ${dtc.code}</span>
        <div class="dtc-info">
          <div class="dtc-name">${dtc.name}</div>
          <div class="dtc-desc">${dtc.description}</div>
        </div>
        <span class="dtc-act">⚠ ${dtc.action}</span>
      </div>

      <!-- Case steps -->
      <div class="case-panel">
        <div class="cp-hd">
          <span class="cp-badge">Case ${ci + 1} of ${dtc.cases.length}</span>
          <div>
            <div class="cp-area-group">${c.area_group}</div>
            <div class="cp-title">${c.area_detail}</div>
          </div>
          <span class="cp-cnt">${c.steps.length} steps</span>
        </div>
        ${stepsHtml}
      </div>

      <!-- Navigation -->
      <div class="case-nav">
        <button class="btn-case" onclick="navigateCase(${ci - 1})" ${ci === 0 ? 'disabled' : ''}>← Previous</button>
        <div class="nav-info">Case ${ci + 1} of ${dtc.cases.length}</div>
        ${isLast
          ? `<button class="btn-case primary" onclick="finishDiag()">Complete & View Summary ✓</button>`
          : `<button class="btn-case primary" onclick="navigateCase(${ci + 1})">Next Case →</button>`
        }
      </div>

      <!-- Export bar -->
      <div class="export-bar">
        <div class="xwrap">
          <button class="btn-exp" onclick="toggleExport()">⬇ Export Report</button>
          <div class="xmenu" id="xmenu">
            <div class="xitem" onclick="doExport('pdf')"><span>📄</span>Download PDF</div>
            <div class="xitem" onclick="doExport('word')"><span>📝</span>Download Word</div>
          </div>
        </div>
        <button class="btn-rst" onclick="showProblemStatement()">↺ New Session</button>
        <div class="exp-status">Session: ${SESS}</div>
      </div>

    </div>`;

  document.getElementById('diagContent').scrollTop = 0;
}


/* ══════════════════════════════════════════════════════════════════════════
   10. ANSWER LOGIC
══════════════════════════════════════════════════════════════════════════ */

function setAns(ci, si, val, el) {
  if (!answers[ci]) answers[ci] = {};

  /* Preserve existing notes */
  const notesEl  = document.getElementById(`notes-${ci}-${si}`);
  const curNotes = notesEl ? notesEl.value : '';
  answers[ci][si] = { value: val, notes: curNotes };

  const card    = document.getElementById(`sc-${ci}-${si}`);
  const flagged = val === 'NO' || val === 'NOT OK';
  const answered = val != null && val !== '';

  /* Deselect all buttons in this card, select clicked one */
  if (el && el.tagName === 'BUTTON') {
    card.querySelectorAll('.yn-btn, .cond-btn').forEach(b => b.classList.remove('sel'));
    el.classList.add('sel');
  }

  card.className = 'step-card ' + (answered ? (flagged ? 'flagged' : 'answered') : '');

  /* Show/hide corrective action and ok message */
  const corr = document.getElementById(`corr-${ci}-${si}`);
  const ok   = document.getElementById(`ok-${ci}-${si}`);
  if (corr) corr.className = 'sc-corrective' + (flagged ? ' show' : '');
  if (ok)   ok.className   = 'sc-ok'          + (answered && !flagged ? ' show' : '');
}

function saveNotes(ci, si, val) {
  if (!answers[ci])       answers[ci]    = {};
  if (!answers[ci][si])   answers[ci][si] = { value: null };
  answers[ci][si].notes = val;
}


