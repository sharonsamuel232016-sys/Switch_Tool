function filterDTCOptions(q) {
  const lq = q.toLowerCase();
  document.querySelectorAll('#fi-dtc-list .dtc-opt').forEach(el => {
    const match = el.dataset.code.includes(lq) || el.dataset.name.includes(lq);
    el.style.display = match ? '' : 'none';
  });
}

/* Keep tags in sync whenever a checkbox changes */
document.addEventListener('change', e => {
  if (e.target.closest('#fi-dtc-list')) syncDTCTags();
});

function syncDTCTags() {
  const tags = document.getElementById('fi-dtc-tags');
  if (!tags) return;
  tags.innerHTML = '';
  document.querySelectorAll('#fi-dtc-list input[type=checkbox]:checked').forEach(cb => {
    const dtc = DTC_LIBRARY[cb.value];
    const t   = document.createElement('span');
    t.style.cssText = 'background:var(--primary);color:#fff;border-radius:20px;padding:3px 10px;font-size:.75rem;display:flex;align-items:center;gap:6px';
    t.innerHTML = `DTC ${dtc.code} <span style="cursor:pointer;opacity:.8" onclick="removeDTCTag('${dtc.code}')">✕</span>`;
    tags.appendChild(t);
  });
}

