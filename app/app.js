function buildSidebar() {
  const list  = document.getElementById('dtcList');
  list.innerHTML = '';

  /* Filter DTC codes for the selected vehicle */
  const codes = Object.values(DTC_LIBRARY).filter(
    d => !d.vehicles || d.vehicles.includes(vehicle)
  );

  document.getElementById('dtcCnt').textContent  = codes.length;
  document.getElementById('sessDisp').textContent = SESS;

  codes.forEach(d => {
    const el = document.createElement('div');
    el.className = 'dtc-card';
    el.innerHTML = `
      <div class="dc-n">DTC ${d.code} · ${d.cases.length} cases</div>
      <div class="dc-t">${d.name}</div>
      <div class="dc-d">${d.description}</div>`;
    el.onclick = () => { activeDTC = d; currentCase = 0; answers = {}; renderCase(0); };
    list.appendChild(el);
  });
}

